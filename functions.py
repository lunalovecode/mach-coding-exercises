def add(num1, num2):
  return num1 + num2

def subtract(num1, num2):
  return num1 - num2

def multiply(num1, num2):
  return num1 * num2

def divide(num1, num2):
  return num1 / num2

def divide_with_remainder(num1, num2):
  return str(round(num1 / num2)) + " R " + str(num1 % num2)

def raise_to_power(num1, num2):
  return num1 ** num2